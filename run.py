    def uploadFunction(self):
        dpath = QFileDialog.getExistingDirectory(self)  # ÏÑ†ÌÉùÌïú ÎîîÎ†âÌÜ†Î¶¨ Í≤ΩÎ°ú
        filter = ['.jpg', '.JPG', '.png', '.PNG', '.bmp', '.BMP', '.jpeg', '.JPEG', '.gif', '.GIF']

        try:
            temp = os.listdir(dpath)  # ÌååÏùºÏù¥Î¶ÑÎßå
        except:
            return 0

        file_list = []
        self.duplites.setText("ÌÉêÏÉâÏ§ë...")

        # Ïù¥ÎØ∏ÏßÄ ÌååÏùºÎßå Î∞õÍ∏∞
        for file in temp:
            for fil in filter:
                if file.endswith(fil):
                    file_list.append(file)

        file_list.reverse()

        img_dict = {}
        file_path = []  # ÌååÏùº Ï†àÎåÄ Í≤ΩÎ°ú Ï†ÑÏ≤¥

        # ÌååÏùºÎì§Ïùò Ï†àÎåÄ Í≤ΩÎ°úÎ•º file_path Î¶¨Ïä§Ìä∏Ïóê Ï†ÄÏû•
        for file in file_list:
            file_path.append((dpath + "/" + file))

        # Ïù¥ÎØ∏ÏßÄ Ìï¥ÏãúÍ∞íÏùÑ img_dictÏóê Ï†ÄÏû•
        for file in file_path:
            hash = ImageHash(file)
            img_dict[file] = hash

        new_dic = {}
        dup_array = []
        for file, hash in img_dict.items():
            if hash not in new_dic.keys():
                new_dic[hash] = file
            else:
                dup_array.append([file, new_dic[hash]])

        self.duplites.setText("[ Ï§ëÎ≥µÎêú ÌååÏùº - ÏõêÎ≥∏ ÌååÏùº ]")
        for file in dup_array:
            self.duplites.append(file[0].split('/')[-1] + " - " + file[1].split('/')[-1])

        self.duplites.append("ÌÉêÏÉâ ÎÅù!")
        self.dpath = dpath
        self.dup_array = dup_array

